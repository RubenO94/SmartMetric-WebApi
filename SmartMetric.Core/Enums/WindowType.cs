/// <summary>
/// Enumeração que representa os tipos de janelas no módulo SmartMetric.
/// Cada valor corresponde a um módulo específico dentro do sistema.
/// </summary>
public enum WindowType
{
    /// <summary>
    /// Janela relacionada a formulários.
    /// </summary>
    Forms = 23121000,

    /// <summary>
    /// Janela relacionada a avaliações (reviews).
    /// </summary>
    Reviews = 23122000,

    /// <summary>
    /// Janela relacionada a estatísticas.
    /// </summary>
    Statistics = 23123000,

    /// <summary>
    /// Janela relacionada a configurações administrativas.
    /// </summary>
    AdminSettings = 23124000,

    /// <summary>
    /// Janela relacionada a perfis de utilizador.
    /// </summary>
    Profiles = 23125000,

    /// <summary>
    /// Janela relacionada a departamentos.
    /// </summary>
    Departments = 23126000,

    /// <summary>
    /// Janela relacionada a contas de utilizador.
    /// </summary>
    Accounts = 23127000,

    /// <summary>
    /// Janela relacionada a submissões.
    /// </summary>
    Submissions = 23128000,
}
